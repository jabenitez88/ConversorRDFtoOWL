    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        boolean debugger = false;
        String[] propiedades = new String[50];
        String[] propiedadesNuevas = new String[50];
        ParserRDFtoOWL parseando = new ParserRDFtoOWL();
        DefaultTableModel model     = (DefaultTableModel) jTable2.getModel();
        int totalProps = jTable2.getRowCount();
            for(int i=0;i<totalProps;i++){
              propiedades[i] = (String) model.getValueAt(i,0);
              propiedadesNuevas[i] = (String) model.getValueAt(i,1);
              //System.out.println("Propiedad Vieja " + propiedades[i]);              
              //System.out.println("Propiedad Nueva " + propiedadesNuevas[i]);

            }
        if (jCheckBox1.isSelected()) {
            debugger = true;
            //System.out.println("SELECCIONAO");
        }
        parseando.Parseador(jTextField3.getText(), jTextField4.getText(), totalProps, jTextField5.getText(), jTextField1.getText(), jTextField2.getText(), propiedades, propiedadesNuevas, this, debugger);
    }//GEN-LAST:event_jButton4ActionPerformed

